#include "delay.h"

/**
  * @函数名       delay_ms
  * @功  能       延迟X * ms
  * @参  数       ms：延迟ms
  * @返回值       无
  */
void delay_ms(uint16_t ms)//延迟函数，MS级别
{
  uint16_t x,y;
  for(x = ms;x>0;x--)
  {
    for(y = 3153;y>0;y--);
  }
}


